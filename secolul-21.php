<h2>Secolul XXI</h2>

<p>Anul trecut în urma voturilor a 157 de mii de cititori ai cotidianului MARCA a fost ales cel mai bun 11 al clubului Real Madrid din toate timpurile. Cu ocazia împlinirii a 110 ani de la infintarea clubului blanco, cotidianul madrilen a realizat un sondaj între cititorii sai.</p>
<p>Votantii au fost invitati sa aleaga cel mai bun unsprezece al istoriei clubului blanco alegând dintre urmatorii jucatori: Casillas, Zamora si Betancort (portari), Roberto Carlos, Hierro, Sergio Ramos, Camacho, Sanchís, Santamaría, Salgado, Chendo, Marquitos (fundasi); Zidane, Puskas, Redondo, Míchel, Pirri, Gordillo, Zoco, Stielike, Martín Vázquez, Molowny, Velázquez, Rial (mijlocasi); Di Stéfano, Raúl, Ronaldo, Hugo Sánchez, Juanito, Butragueño, Gento, Cristiano Ronaldo, Amancio si Kopa (atacanti).</p>
<p>Cel mai votat jucator a fost Zinedine Zidane (152.416 voturi) urmat de Roberto Carlos (141.977 voturi). Din actuala echipa a lui Mourinho, Sergio Ramos, Cristiano Ronaldo si Iker Casillas se regasesc în cel mai bun unsprezece al madrilenilor. De asemenea batrânii Puskas si Di Stefano sunt în continuare în gratiile suporterilor prin rezultatele obtinute în perioada când au jucat. Urmeaza un portret al celor trei actuali jucatori care se regasesc în aceata echipa a tuturor timpurilor.</p>

