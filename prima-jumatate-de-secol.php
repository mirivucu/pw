<h2>Prima jumatate de secol</h2>

<p>La trei ani dupa fondarea sa, în 1905, Madrid FC a câstigat primul titlu dupa ce a învins-o pe Athletic Bilbao în finala Cupei Spaniei. Clubul a devenit unul dintre membrii fondatori ai Federatiei Regale Spaniole de Fotbal pe 4 ianuarie 1909, atunci când presedintele clubului, Adolfo Meléndez, a semnat acordul de fondare a asociatiei de fotbal spaniole. Dupa ce a jucat pe mai multe stadioane, echipa s-a mutat pe Campo de O'Donnell în 1912. În 1920, numele clubului a fost schimbat în Real Madrid, dupa ce regele Alfonso al XIII-lea al Spaniei a acordat titlul de Real (Regal) clubului.</p>
<p>În 1929 a fost fondata prima liga de fotbal spaniola. Real Madrid a condus în prima editie pâna la ultimul meci, o înfrângere în fata lui Athletic Bilbao rezultând în clasarea pe locul doi, dupa Barcelona. Real Madrid a câstigat primul titlu al ligii în sezonul 1931-1932. Albii au câstigat din nou liga în anul urmator, devenind prima echipa care a câstigat campionatul de doua ori.</p>
<p>Santiago Bernabéu Yeste a devenit presedintele Real Madridului în 1945. Sub presedintia sa clubul, stadionul Santiago Bernabéu si Ciudad Deportiva au fost reconstruite dupa încheierea Razboiului Civil Spaniol. Începând cu 1953, el a abordat o noua strategie, aceea de a semna cu jucatori de clasa mondiala din strainatate, cea mai proeminenta realizare fiind aducerea lui Alfredo Di Stéfano.</p>

